package com.wt.openapi.p1.model;

public class EmotionVo {
	private String emNo;
    private String memNo;
    private String emValue;
    private String emCase;
    private String emContent;
    private String createDate;
    private String modifyDate;
    
	public String getEmNo() {
		return emNo;
	}
	public void setEmNo(String emNo) {
		this.emNo = emNo;
	}
	public String getMemNo() {
		return memNo;
	}
	public void setMemNo(String memNo) {
		this.memNo = memNo;
	}
	public String getEmValue() {
		return emValue;
	}
	public void setEmValue(String emValue) {
		this.emValue = emValue;
	}
	public String getEmCase() {
		return emCase;
	}
	public void setEmCase(String emCase) {
		this.emCase = emCase;
	}
	public String getEmContent() {
		return emContent;
	}
	public void setEmContent(String emContent) {
		this.emContent = emContent;
	}
	public String getCreateDate() {
		return createDate;
	}
	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}
	public String getModifyDate() {
		return modifyDate;
	}
	public void setModifyDate(String modifyDate) {
		this.modifyDate = modifyDate;
	}
}
